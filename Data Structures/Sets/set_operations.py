# adding item
x = {1,1,2,2,3,3}
x.add(4)
print(x)

# removing item
y = {"a","a","b","b","c","c"}
y.remove("a")
print(y)

# checking membership
fruits = {"apple","mango","banana","pineapple"}
check = "apple" in fruits
print(check)

# Cardinality/Length
persons = {"ram","ram","shyam","shyam","hari"}
print(len(persons))

# pop random item
k = {"@",")","a","b"}
poped_item = k.pop()
print(poped_item)

# delete items from set
temp = {"a","b","c","d"}
temp.clear()
print(temp)

